package Modelos;

/**
 *
 * @author Michel
 */
public class Locadora {
    private String nomeDVD;
    private String secaoDVD;
    private String sinopseDVD;
    private int idDVD;

    public String getNomeDVD() {
        return nomeDVD;
    }

    public void setNomeDVD(String nomeDVD) {
        this.nomeDVD = nomeDVD;
    }

    public String getSecaoDVD() {
        return secaoDVD;
    }

    public void setSecaoDVD(String secaoDVD) {
        this.secaoDVD = secaoDVD;
    }

    public String getSinopseDVD() {
        return sinopseDVD;
    }

    public void setSinopseDVD(String sinopseDVD) {
        this.sinopseDVD = sinopseDVD;
    }

    public int getIdDVD() {
        return idDVD;
    }

    public void setIdDVD(int idDVD) {
        this.idDVD = idDVD;
    }
    
    
}
